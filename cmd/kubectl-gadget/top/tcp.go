type TCPFlags struct {
	CommonTopFlags

	ParsedSortBy types.SortBy
	FilteredPid  uint
	Family       uint
}

type TCPParser struct {
	commonutils.BaseParser[types.Stats]
	sync.Mutex

	flags     *TCPFlags
	nodeStats map[string][]types.Stats
}

func newTCPCmd() *cobra.Command {
	var flags TCPFlags

	commonFlags := &utils.CommonFlags{
		OutputConfig: commonutils.OutputConfig{
			// The columns that will be used in case the user does not specify
			// which specific columns they want to print.
			CustomColumns: []string{
				"node",
				"namespace",
				"pod",
				"container",
				"pid",
				"comm",
				"family",
				"saddr",
				"daddr",
				"sent",
				"received",
			},
		},
	}

	columnsWidth := map[string]int{
		"node":      -16,
		"namespace": -16,
		"pod":       -30,
		"container": -16,
		"pid":       -7,
		"comm":      -16,
		"family":    -3,
		"saddr":     -51,
		"daddr":     -51,
		"sent":      -7,
		"received":  -7,
	}

	cmd := &cobra.Command{
		Use:   fmt.Sprintf("tcp [interval=%d]", types.IntervalDefault),
		Short: "Periodically report TCP activity",
		RunE: func(cmd *cobra.Command, args []string) error {
			var err error

			parser := &TCPParser{
				BaseParser: commonutils.NewBaseWidthParser[types.Stats](columnsWidth, &commonFlags.OutputConfig),
				flags:      &flags,
				nodeStats:  make(map[string][]types.Stats),
			}
			if len(args) == 1 {
				flags.OutputInterval, err = strconv.Atoi(args[0])
				if err != nil {
					return commonutils.WrapInErrInvalidArg("<interval>",
						fmt.Errorf("%q is not a valid value", args[0]))
				}
			} else {
				flags.OutputInterval = types.IntervalDefault
			}

			parameters := map[string]string{
				types.MaxRowsParam:  strconv.Itoa(flags.MaxRows),
				types.IntervalParam: strconv.Itoa(flags.OutputInterval),
				types.SortByParam:   flags.SortBy,
			}

			if flags.Family != 0 {
				parameters[types.FamilyParam] = strconv.FormatUint(uint64(flags.Family), 10)
			}

			if flags.FilteredPid != 0 {
				parameters[types.PidParam] = strconv.FormatUint(uint64(flags.FilteredPid), 10)
			}

			config := &utils.TraceConfig{
				GadgetName:       "tcptop",
				Operation:        "start",
				TraceOutputMode:  "Stream",
				TraceOutputState: "Started",
				CommonFlags:      commonFlags,
				Parameters:       parameters,
			}

			// when params.Timeout == interval it means the user
			// only wants to run for a given amount of time and print
			// that result.
			singleShot := commonFlags.Timeout == flags.OutputInterval

			// start print loop if this is not a "single shot" operation
			if singleShot {
				parser.PrintHeader()
			} else {
				parser.StartPrintLoop()
			}

			if err := utils.RunTraceStreamCallback(config, parser.Callback); err != nil {
				return commonutils.WrapInErrRunGadget(err)
			}

			if singleShot {
				parser.PrintEvents()
			}

			return nil
		},
		SilenceUsage: true,
		PreRunE: func(cmd *cobra.Command, args []string) error {
			var err error
			flags.ParsedSortBy, err = types.ParseSortBy(flags.SortBy)
			if err != nil {
				return commonutils.WrapInErrInvalidArg("--sort", err)
			}

			return nil
		},
		Args: cobra.MaximumNArgs(1),
	}

	addCommonTopFlags(cmd, &flags.CommonTopFlags, commonFlags, types.MaxRowsDefault, types.SortBySlice)

	cmd.PersistentFlags().UintVarP(
		&flags.FilteredPid,
		"pid",
		"",
		0,
		"Show only TCP events generated by this particular PID",
	)
	cmd.PersistentFlags().UintVarP(
		&flags.Family,
		"family",
		"f",
		0,
		"Show only TCP events for this IP version: either 4 or 6 (by default all will be printed)",
	)

	return cmd
}

func (p *TCPParser) Callback(line string, node string) {
	p.Lock()
	defer p.Unlock()

	var event types.Event

	if err := json.Unmarshal([]byte(line), &event); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %s", commonutils.WrapInErrUnmarshalOutput(err, line))
		return
	}

	if event.Error != "" {
		fmt.Fprintf(os.Stderr, "Error: failed on node %q: %s", event.Node, event.Error)
		return
	}

	p.nodeStats[node] = event.Stats
}

func (p *TCPParser) StartPrintLoop() {
	go func() {
		ticker := time.NewTicker(time.Duration(p.flags.OutputInterval) * time.Second)
		p.PrintHeader()
		for {
			_ = <-ticker.C
			p.PrintHeader()
			p.PrintEvents()
		}
	}()
}

func (p *TCPParser) PrintHeader() {
	switch p.OutputConfig.OutputMode {
	case commonutils.OutputModeColumns:
		if term.IsTerminal(int(os.Stdout.Fd())) {
			utils.ClearScreen()
		} else {
			fmt.Println("")
		}
		fmt.Printf("%-16s %-16s %-16s %-16s %-7s %-16s %-3s %-51s %-51s %-7s %s\n",
			"NODE", "NAMESPACE", "POD", "CONTAINER",
			"PID", "COMM", "IPv", "LADDR", "RADDR", "RX_KB", "TX_KB")
	case commonutils.OutputModeCustomColumns:
		if term.IsTerminal(int(os.Stdout.Fd())) {
			utils.ClearScreen()
		} else {
			fmt.Println("")
		}
		fmt.Println(p.GetCustomColsHeader(p.OutputConfig.CustomColumns))
	}
}

func (p *TCPParser) PrintEvents() {
	// sort and print events
	p.Lock()

	stats := []types.Stats{}
	for _, stat := range p.nodeStats {
		stats = append(stats, stat...)
	}
	p.nodeStats = make(map[string][]types.Stats)

	p.Unlock()

	types.SortStats(stats, p.flags.ParsedSortBy)

	switch p.OutputConfig.OutputMode {
	case commonutils.OutputModeColumns:
		for idx, event := range stats {
			if idx == p.flags.MaxRows {
				break
			}

			tcpFamily := 4
			if event.Family == syscall.AF_INET6 {
				tcpFamily = 6
			}

			fmt.Printf("%-16s %-16s %-16s %-16s %-7d %-16s %-3d %-51s %-51s %-7d %d\n",
				event.Node, event.Namespace, event.Pod, event.Container,
				event.Pid, event.Comm, tcpFamily,
				fmt.Sprintf("%s:%d", event.Saddr, event.Sport),
				fmt.Sprintf("%s:%d", event.Daddr, event.Dport),
				event.Received/1048, event.Sent/1048)
		}
	case commonutils.OutputModeJSON:
		b, err := json.Marshal(stats)
		if err != nil {
			fmt.Fprintf(os.Stderr, "Error: %s", commonutils.WrapInErrMarshalOutput(err))
			return
		}
		fmt.Println(string(b))
	case commonutils.OutputModeCustomColumns:
		for idx, stat := range stats {
			if idx == p.flags.MaxRows {
				break
			}
			fmt.Println(p.FormatEventCustomCols(&stat, p.OutputConfig.CustomColumns))
		}
	}
}

func (p *TCPParser) GetCustomColsHeader(cols []string) string {
	var sb strings.Builder

	for _, col := range cols {
		switch col {
		case "node":
			sb.WriteString(fmt.Sprintf("%-16s", "NODE"))
		case "namespace":
			sb.WriteString(fmt.Sprintf("%-16s", "NAMESPACE"))
		case "pod":
			sb.WriteString(fmt.Sprintf("%-16s", "POD"))
		case "container":
			sb.WriteString(fmt.Sprintf("%-16s", "CONTAINER"))
		case "pid":
			sb.WriteString(fmt.Sprintf("%-7s", "PID"))
		case "comm":
			sb.WriteString(fmt.Sprintf("%-16s", "COMM"))
		case "family":
			sb.WriteString(fmt.Sprintf("%-3s", "IPv"))
		case "saddr":
			sb.WriteString(fmt.Sprintf("%-51s", "LADDR"))
		case "daddr":
			sb.WriteString(fmt.Sprintf("%-51s", "DADDR"))
		case "sent":
			sb.WriteString(fmt.Sprintf("%-7s", "TX_KB"))
		case "received":
			sb.WriteString(fmt.Sprintf("%-7s", "RX_KB"))
		}
		sb.WriteRune(' ')
	}

	return sb.String()
}

func (p *TCPParser) FormatEventCustomCols(stats *types.Stats, cols []string) string {
	var sb strings.Builder

	for _, col := range cols {
		switch col {
		case "node":
			sb.WriteString(fmt.Sprintf("%-16s", stats.Node))
		case "namespace":
			sb.WriteString(fmt.Sprintf("%-16s", stats.Namespace))
		case "pod":
			sb.WriteString(fmt.Sprintf("%-16s", stats.Pod))
		case "container":
			sb.WriteString(fmt.Sprintf("%-16s", stats.Container))
		case "pid":
			sb.WriteString(fmt.Sprintf("%-7d", stats.Pid))
		case "comm":
			sb.WriteString(fmt.Sprintf("%-16s", stats.Comm))
		case "family":
			tcpFamily := 4
			if stats.Family == syscall.AF_INET6 {
				tcpFamily = 6
			}

			sb.WriteString(fmt.Sprintf("%-3d", tcpFamily))
		case "saddr":
			sb.WriteString(fmt.Sprintf("%-51s", fmt.Sprintf("%s:%d", stats.Saddr, stats.Sport)))
		case "daddr":
			sb.WriteString(fmt.Sprintf("%-51s", fmt.Sprintf("%s:%d", stats.Daddr, stats.Dport)))
		case "sent":
			sb.WriteString(fmt.Sprintf("%-7d", stats.Sent))
		case "received":
			sb.WriteString(fmt.Sprintf("%d", stats.Received))
		}
		sb.WriteRune(' ')
	}

	return sb.String()
}
